#include <bits/stdc++.h>
using namespace std;

void solve() {
  int n, m;
  string s;
  cin >> n >> m >> s;
  while (m--) {
    int x;
    cin >> x;
    if (x == 1) {
      int k;
      char x;
      cin >> k >> x;
    } else {
      int a, b;
      cin >> a >> b;
    }
  }
}

int32_t main() {
  int t = 1;
  cin >> t;
  while (t--) solve();
}
